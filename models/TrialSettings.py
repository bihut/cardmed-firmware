class TrialSettings:
    def __init__(self, trial_name, trial_type, trial_description, trial_start_date, trial_end_date, trial_status):
        self.trial_name = trial_name
        self.trial_type = trial_type
        self.trial_description = trial_description
        self.trial_start_date = trial_start_date
        self.trial_end_date = trial_end_date
        self.trial_status = trial_status